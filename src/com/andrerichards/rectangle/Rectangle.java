package com.andrerichards.rectangle;

public class Rectangle {
    // constructor chaining example
    private int x;
    private int y;
    private int width;
    private int height;

    public Rectangle() {

    }

    public Rectangle(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
